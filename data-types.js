// There are 8 basic data types in Javascript - number, string, bigint , boolean , undefined , null , symbol , object

// Number - represents both integers and floating point numbers 
// Bigint - represents integers of arbitrary length
// String - a string must be surrounded by quotes " ' or ~
// Boolean - The boolean type has only 2 values - true and false 
// null - represents nothing or empty
// undefined value is not assigned 
// Object - All other types are called primitives because their values only contain a singel thing . Objects are used to store collections of data 

